export function makeStructuredMismatchRow({
  locationSet,
  monthsPerYear,
  year,
  month,
  location,
  latBin,
  lonBin,
  minutePg,
  expectedSigns,
  actualSigns,
  actualLongitudes1e9,
  oracleLongitudesDeg,
  mismatchMask,
}) {
  return {
    type: "mismatch_point",
    locationSet,
    monthsPerYear,
    year,
    month,
    location,
    latBin,
    lonBin,
    minutePg,
    expectedSigns,
    actualSigns,
    actualLongitudes1e9,
    oracleLongitudesDeg,
    mismatchMask,
    planetMismatch: (mismatchMask & 0x7f) !== 0,
    ascMismatch: (mismatchMask & 0x80) !== 0,
  };
}

